/**
 * @brief This function handles TIM3 global interrupt.
 */
void TIM3_IRQHandler(void) {
	/* USER CODE BEGIN TIM3_IRQn 0 */

	// this timer handles the PWM of the channels, turning on and off according to set duty cycle
	// loop around between 0-100 to restart duty_cycle
	dc_count = (dc_count + 1) % (MAX_DUTY_CYCLE + 1);

	uint32_t current_flag = 0;
	for (uint8_t i = 0; i < 32; i++) {
		current_flag = (1 << i);
		if (pwm_channels & current_flag) {
			if (dc_count < current_dutycycles[i]) {
				current_state |= current_flag;
			} else {
				current_state &= ~current_flag;
			}

		} else {
			// pwm is off for this channel
			current_dutycycles[i] = 0;
			// grab current PWM channels and explictly turn this bit off
			current_state = ((current_state & PWM_CHANNELS) & ~(current_flag));
			// set non PWM pins as per user's request
			current_state = current_state | user_channels;
		}
	}

	set_channel_states(current_state);
	/* USER CODE END TIM3_IRQn 0 */
	HAL_TIM_IRQHandler(&htim3);
	/* USER CODE BEGIN TIM3_IRQn 1 */

	/* USER CODE END TIM3_IRQn 1 */
}

/**
 * @brief This function handles TIM4 global interrupt.
 */
void TIM4_IRQHandler(void) {
	/* USER CODE BEGIN TIM4_IRQn 0 */

	// this timer handles gradually increasing current duty cycle of PWM channels to the request user duty cycle
	// this is done to avoid inrush of current caused by yeeting the duty cycle straight to full
	for (int i = 0; i < 32; i++) {
		if (current_dutycycles[i] < user_dutycycles[i]) {
			current_dutycycles[i]++;
		} else {
			current_dutycycles[i] = user_dutycycles[i];
		}
	}
	/* USER CODE END TIM4_IRQn 0 */
	HAL_TIM_IRQHandler(&htim4);
	/* USER CODE BEGIN TIM4_IRQn 1 */

	/* USER CODE END TIM4_IRQn 1 */
}

